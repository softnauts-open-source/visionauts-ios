import Foundation
import UIKit

extension UIColor {
    class var backgroundColor: UIColor {
        return UIColor(red: 26/255, green: 38/255, blue: 107/255, alpha: 1.0)
    }
    
    class var lightRoyalBlue: UIColor {
        return UIColor(red: 47/255, green: 76/255, blue: 251/255, alpha: 1.0)
    }
    
    class var redPink: UIColor {
        return UIColor(red: 255/255, green: 33/255, blue: 96/255, alpha: 1.0)
    }
    
    class var hotGreen: UIColor {
        return UIColor(red: 42/255, green: 255/255, blue: 33/255, alpha: 1.0)
    }
}
